	LEFT JOIN [BR_TD_VITAIT].dbo.[Nodes] nodes
		ON resp.NodeID = nodes.NodeID
	WHERE nodes.Nome_do_cliente like ''%BRADESCO%''
	'
) AS ResponseTime

